package kz.astana.uvaissov.insta.cabinet.model;

public class AnalyticModel {
	private int totalRedirect;
	private int weekRedirect;
	private int dayRedirect;
	public int getTotalRedirect() {
		return totalRedirect;
	}
	public void setTotalRedirect(int totalRedirect) {
		this.totalRedirect = totalRedirect;
	}
	public int getWeekRedirect() {
		return weekRedirect;
	}
	public void setWeekRedirect(int weekRedirect) {
		this.weekRedirect = weekRedirect;
	}
	public int getDayRedirect() {
		return dayRedirect;
	}
	public void setDayRedirect(int dayRedirect) {
		this.dayRedirect = dayRedirect;
	}
	
}

